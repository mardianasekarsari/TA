public class User {
    String username, nama, nohp, rumahpompa;

    public User() {
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNohp() {
        return nohp;
    }

    public void setNohp(String nohp) {
        this.nohp = nohp;
    }

    public String getRumahpompa() {
        return rumahpompa;
    }

    public void setRumahpompa(String rumahpompa) {
        this.rumahpompa = rumahpompa;
    }
}
