public class Petugas {
    String nama, nohp, rumahpompa;

    public Petugas() {
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNohp() {
        return nohp;
    }

    public void setNohp(String nohp) {
        this.nohp = nohp;
    }

    public String getRumahpompa() {
        return rumahpompa;
    }

    public void setRumahpompa(String rumahpompa) {
        this.rumahpompa = rumahpompa;
    }
}
